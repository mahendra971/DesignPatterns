package Bridge.Movie;

public class Movie {

    private String name;
    private String year;
    private String time;


    public String getYear() {
        return year;
    }


    public String getName() {
        return name;
    }


    public String getTime() {
        return time;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public void setTime(String time) {
        this.time = time;
    }


}
